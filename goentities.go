/*
 Small package providing (X)HTML entity encoding and decoding

 Decode strings with DecodeString, encode them with EncodeString. By default EncodeString uses an extended set of name mappings.

 Additional EncodeString functions are available if different sets of name mappings are required, or if it's necessary to encode entities using hexadecimal instead of decimal.


 */

package entities

import (
	"fmt"
	"strconv"
	"flag"
	"strings"
)

// Types of encoders. 'Named' is implicitly named entities plus decimal encoding; NamedOnly if you really only want
// named entities.
type EncoderStyle int
const(
	Decimal EncoderStyle = iota
	Hexadecimal
	Named
	NamedHexadecimal
	NamedOnly
	InvalidEncoderStyle
)


type Encoder struct{
	EntityMap map[int] string
	Style EncoderStyle
}


func decodeEntity(in string) (out uint64, err bool){
	out2,ok := expandedEntityMap[in]
	out = uint64(out2)
	if !ok {
		// Not a named entity; extract number string and convert
		err = true
		if in[0] == '#'{
			if in[1] == 'x' || in[1] == 'X' {
				in = "0" + in[1:len(in)]
			}else{
				in = in[1:len(in)]
			}
			numeric, numErr := strconv.Btoui64(in,0)
			if numErr == nil{
				out = numeric
				err = false
			}
		}
	}
	return
}

func DecodeString(in string) (out string){    
	out = ""
	inEntity := false
	entityStart := 0
	for i, rune := range(in){
     		if !inEntity{
			if rune == '&' {
				inEntity = true
				entityStart = i + 1
			}else{
				out += string(rune)
			}
		}else if rune==';'{
			entity,err := decodeEntity(in[entityStart:i])
			if !err{
				out += string(entity)
			}else{
				out += "&" + in[entityStart:i] + ";"
			}
			inEntity = false
		}
	}
	return
}

func encodeString(in string, entityMap map[int] string, encoderStyle EncoderStyle) (out string){
	out = ""
	for _,rune := range(in){
		encoded,ok := entityMap[rune]
		if encoderStyle == Named || encoderStyle == NamedOnly || encoderStyle == NamedHexadecimal{
			if ok {
				out += "&" + encoded + ";"
				continue
			}
		}
		if encoderStyle != NamedOnly && (rune < 0x20 || rune > 0x7E || ok ){
			encoded := ""
			switch(encoderStyle){
			case Decimal, Named: 
				encoded = strconv.Itob(int(rune),10)
			case Hexadecimal, NamedHexadecimal: 
				encoded = "x" + strconv.Itob(int(rune),16)
			}
			out += "&#" + encoded + ";"
		}else{
			out += string(rune)
		}
	}
	return 
}

func (c *Encoder)EncodeString(in string)(out string){
	return encodeString(in, c.EntityMap,c.Style)
}

// Encode a string using the extended entity set
func EncodeString(in string)(out string, status bool){
	return encodeString(in, reverseExpandedEntityMap, Named), true
}

// Encode a string, using a specified entity set (html4, xhtml, expanded, basic)
func EncodeStringSpec(in string, spec string)(out string, status bool){
	return EncodeStringSpecStyle(in, spec, "named")
}

// Encode a string using a specified style (named, decimal, hexadecimal, named-hexadecimal, namedonly)
func EncodeStringStyle(in string, style string)(out string, status bool){
	return EncodeStringSpecStyle(in, "expanded", style)
}

// Encode a string using both a specified style and a specified entity set
func EncodeStringSpecStyle(in string, spec string, style string)(out string, status bool){
	encodeStyle := getStyle(style)
	encodeSpec := getSpec(spec)
	if encodeStyle != InvalidEncoderStyle && encodeSpec != nil{
		return encodeString(in, encodeSpec, encodeStyle), true
	}
	return "", false
}

func getStyle(in string)(out EncoderStyle){
	style := strings.ToLower(in)
	
	switch(style){
	case "decimal": out = Decimal
	case "hexadecimal": out = Hexadecimal
	case "named", "named-decimal": out = Named
	case "named-hexadecimal": out = NamedHexadecimal
	default: out = InvalidEncoderStyle
	}
	return
}

func getSpec(in string)(out map[int] string){
	spec := strings.ToLower(in)

	switch(spec){
	case "basic": out = reverseBasicEntityMap
	case "xhtml1": out = reverseXHTMLEntityMap
	case "html4": out = reverseHTML4EntityMap
	case "expanded": out = reverseExpandedEntityMap
	default: out =  nil
	}
	return
}

// Create an encoder object that remembers the desired encoder settings
// Use if you repeatedly want to encode strings with non-default behaviour
func InitializeEncoder(style string, spec string)(*Encoder){
	var c Encoder;

	c.Style = getStyle(style)
	if(c.Style == InvalidEncoderStyle){
		fmt.Println("Invalid encoder style specified")
		return nil
	}
	c.EntityMap = getSpec(spec)
	if(c.EntityMap == nil){
		fmt.Println("Invalid specification specified")
		return nil
	}

	return &c
}

